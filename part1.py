import pandas as pd
import numpy as np

# 1. Создаем DataFrame с оценками учеников по 5 предметам
data = {
    'Имя': ['Аня', 'Боб', 'Чарли', 'Лиза', 'Настя', 'Макс', 'Оля', 'Петя', 'Рита', 'Саша'],
    'Математика': [85, 90, 78, 92, 88, 76, 95, 89, 83, 91],
    'Физика': [80, 85, 79, 90, 87, 76, 93, 88, 82, 86],
    'Химия': [75, 80, 70, 85, 78, 65, 88, 82, 77, 84],
    'Литература': [90, 88, 85, 87, 92, 81, 86, 90, 88, 85],
    'История': [88, 85, 82, 90, 87, 84, 89, 91, 86, 83]
}

df = pd.DataFrame(data)

# 2. Выводим первые несколько строк DataFrame
print("Первые несколько строк DataFrame:")
print(df.head())

# 3. Вычисляем среднюю оценку по каждому предмету
print("\nСредняя оценка по каждому предмету:")
print(df.mean(numeric_only=True))

# 4. Вычисляем медианную оценку по каждому предмету
print("\nМедианная оценка по каждому предмету:")
print(df.median(numeric_only=True))

# 5. Вычисляем Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"\nQ1 по математике: {Q1_math}")
print(f"Q3 по математике: {Q3_math}")
print(f"IQR по математике: {IQR_math}")

# 6. Вычисляем стандартное отклонение по каждому предмету
print("\nСтандартное отклонение по каждому предмету:")
print(df.std(numeric_only=True))
